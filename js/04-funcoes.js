function soma(a,b) {
    return a+b;
}
function subtracao(a,b) {
    return a-b;
}
function multiplicacao(a,b) {
    return a*b;
}
function divisao(a,b) {
    return a/b;
}


var resultadoSoma = soma(8,4)
console.log("SOMA: "+ resultadoSoma)

var resultadoSubtracao = subtracao(8,4)
console.log("SUBTRACAO: "+ resultadoSubtracao)

var resultadoDivisao = multiplicacao(8,4)
console.log("MULTIPLICACAO: "+ resultadoDivisao)

var resultadoMultiplicacao = divisao(8,4)
console.log("DIVISAO: "+ resultadoMultiplicacao)

